!**s/r yyg_xchng_8 - Interpolate and exchange scalars fields
!                    from q to q points

      subroutine yyg_xchng_8 ( F_src, F_comm, Minx,Maxx,Miny,Maxy,&
                                      F_ni, F_nj, Nk, F_interpo_S,&
                                      mono_L, do_xch )
      use ISO_C_BINDING
      use yyg_param
      use, intrinsic :: iso_fortran_env
      implicit none

      character(len=*), intent(in)  :: F_interpo_S
      logical, intent(in) :: mono_L, do_xch
      integer, intent(in) :: Minx,Maxx,Miny,Maxy, Nk, F_ni,F_nj
      real(kind=REAL64), intent(inout) :: F_src (Minx:Maxx,Miny:Maxy,Nk)
      type(YYG_comm_param), intent(inout) :: F_comm

      integer :: HLT_np, HLT_start, HLT_end
      integer :: ii,jj,k,kk,m,mm,adr
!
!----------------------------------------------------------------------
!
      call yyg_SendRecv_8 ( F_src, F_comm, Minx,Maxx,Miny,Maxy, &
                            NK, F_interpo_S, mono_L )

! Fill my results buffers if I have received something
      if (F_comm%maxrecv > 0) then

!!$omp do
         do kk=1, F_comm%recvmaxproc
            mm=0
            do m= 1,F_comm%recv_len(kk)
               adr= F_comm%recv_adr(kk)+m
               ii = F_comm%recv_i(adr)
               jj = F_comm%recv_j(adr)
               do k=1,Nk
                  mm=mm+1
                  F_src(ii,jj,k) = YYG_uvrecv(mm,KK)
               end do
            end do
        end do
!!$omp enddo

      end if

      if (do_xch) then
         call HLT_split (1, Nk, HLT_np, HLT_start, HLT_end)
         call gem_xch_halo_8 ( F_src(l_minx,l_miny,HLT_start),&
                               Minx,Maxx,Miny,Maxy, HLT_np,-1)
      endif
!
!----------------------------------------------------------------------
!
      return
      end subroutine yyg_xchng_8

