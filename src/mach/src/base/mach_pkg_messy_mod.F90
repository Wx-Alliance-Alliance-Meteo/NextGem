  subroutine jval_solar_time_control(cdisse, val)

    implicit none
    ! I/O
    real(kind=4),               intent(in)           :: cdisse
    real(kind=4), dimension(:), intent(in), optional :: val

    ! LOCAL
    real(kind=4)                 :: r_sol, r_sol_m1
    integer(kind=4)              :: val_size
    character(len=24), parameter :: substr = 'jval_solar_time_control '
    character(len=256)           :: mesg

    if (present(val)) then
       val_size = size(val)
    else
       val_size = 0
    end if
!
      ! Fluxes must be adjusted to the Sun-Earth distance using the
      ! factor cdisse ("DIStance-Sun-Earth" in AU).
      ! f0 is a ratio of fluxes, cdisse cancels out.
    if (val_size <= 1) then
       if (present(val)) then
          r_sol = (val(1) - 70.0) / 200.0
       else
          r_sol = r_sol_interp
       end if
        ! The 10.7 cm solar radio flux (F10.7cm, in sfu = solar flux
        ! units) changes from about 70 sfu (1E-22 W m?~V~R~H~R2 Hz ?~V~R~H~R1)
        ! to about 270 sfu during the solar cycle (see Fig. 1 in Tapping, Space
        ! Weather, 11, 394-406 (2013)). Thus, the current position in
        ! the solar cycle r_sol is:
       r_sol_m1 = (1.0 - r_sol)
       flux(:) = (r_sol * fmax(:) + r_sol_m1 * fmin(:)) * cdisse
       SR_toa_flux = (r_sol * 9.6701e+12 + r_sol_m1 * 8.7990e+12) * cdisse
       phi_la = (r_sol * 7.0e+11 + r_sol_m1 * 3.0e+11) * cdisse
      ! ratio of fluxes; cdisse cancels out !
       f0(:) = r_sol * f0max + r_sol_m1 * f0min
!
    else if (val_size == 16) then
       flux(:) = val(3:9) * cdisse
       SR_toa_flux =  val(2) * cdisse
       phi_la = val(1) * cdisse
       f0(:) = val(10:16)
!
    else
       write(mesg, "(3a,i5,a)") substr, &
                               ': ERROR IN CHOICE OF jval_solar (CPL)'// &
                              &'! Number of parameters is ', val_size,  &
                               ' but only 1 or 16 are currently implemented.'
        call chm_stop(mesg, -1)
        return
    end if

    return
 end subroutine jval_solar_time_control

