SUBROUTINE ros_PrepareMatrix_night ( H, gam, &
             Jac0, Ghimj, Singular )
! --- --- --- --- --- --- --- --- --- --- --- --- ---
!  Prepares the LHS matrix for stage calculations
!  1.  Construct Ghimj = 1/(H*ham) - Jac0
!      "(Gamma H) Inverse Minus Jacobian"
!  2.  Repeat LU decomposition of Ghimj until successful.
!       -half the step size if LU decomposition fails and retry
!       -exit after 5 consecutive fails
! --- --- --- --- --- --- --- --- --- --- --- --- ---
   IMPLICIT NONE
!~~~> Input arguments
   real(kind=8), INTENT(IN) ::  Jac0(LU_NONZERO)
   real(kind=8), INTENT(IN) ::  gam
!~~~> Output arguments
   real(kind=8), INTENT(OUT) :: Ghimj(LU_NONZERO)
   LOGICAL, INTENT(OUT) ::  Singular
!~~~> Inout arguments
   real(kind=8), INTENT(INOUT) :: H
!~~~> Local variables
   INTEGER(KIND=4)  :: i, ising, Nconsecutive
   real(kind=8) :: ghinv
   real(kind=8), PARAMETER :: ONE  = 1.0d0, HALF = 0.5d0
! Sparse Jacobian Data

  INTEGER, PARAMETER, DIMENSION(41) :: LU_DIAG = (/ &
       1,  2,  7, 11, 14, 17, 19, 23, 26, 28, 30, 32, &
      36, 43, 53, 62, 71, 77, 82, 87, 97,107,114,119, &
     125,139,154,163,181,203,221,256,279,299,317,347, &
     366,391,405,428,429 /)

   Nconsecutive = 0
   Singular = .TRUE.
   DO WHILE (Singular)
!~~~>    Construct Ghimj = 1/(H*gam) - Jac0
     Ghimj = -ONE*Jac0
     ghinv = ONE/(H*gam)
     DO i=1,N
       Ghimj(LU_DIAG(i)) = Ghimj(LU_DIAG(i))+ghinv
     END DO
!~~~>    Compute LU decomposition
     CALL KppDecomp_night ( Ghimj, ising, N, LU_NONZERO )
     IF (ising == 0) THEN
!~~~>    If successful done
        Singular = .FALSE.
     ELSE
!~~~>    If unsuccessful half the step size; if 5 consecutive fails then return
        Nconsecutive = Nconsecutive+1
        Singular = .TRUE.
        PRINT*,'Warning: LU Decomposition returned ising = ',ising
        IF (Nconsecutive <= 5) THEN
           H = H*HALF
        ELSE
           RETURN
        END IF
      END IF
   END DO

  RETURN
END SUBROUTINE ros_PrepareMatrix_night

