SUBROUTINE Jac_SP_night ( V, F, RCT, BGS, JVS, NVAR, NFIX, NREACT, nprcf, LU_NONZERO )
  IMPLICIT NONE
  integer(kind=4), intent(in) :: NVAR, NFIX, NREACT, nprcf, LU_NONZERO
! V - Concentrations of variable species (local)
  REAL(kind=8), intent(in) :: V(NVAR)
! F - Concentrations of fixed species (local)
  REAL(kind=8), intent(in) :: F(NFIX)
