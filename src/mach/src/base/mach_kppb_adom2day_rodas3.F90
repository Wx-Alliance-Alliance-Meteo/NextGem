!               rconst   -> Reaction rate coefficients
!               bgs      -> Reaction product stochiometric coefficients
!               AbsTol   -> Absolute tolerance for solution convergence used by the solver
!               RelTol   -> Relative tolerance for solution convergence used by the solver
!
!             INOUT
!               Y       -> Concentrations of gaseous species (ppmv)
!               H       -> Initial guess for solver timestep interval (min) at the entry &
!                          exit of this subroutine; within the core of the subroutine, H means
!                          the actual time step interval used for the numerical integration
!             OUT
!               IERR    -> Error code
!
!============================================================================
!
!!if_on
SUBROUTINE mach_kppb_adom2day_rodas3(N, Y, nfix, fix, nreact, rconst, nprcf, bgs, &
                     delt, H, RelTol, AbsTol, IERR)
!!if_off
   IMPLICIT NONE
!!if_on
   integer(kind=4), intent(in) :: N, nfix, nreact, nprcf
   REAL(kind=8), INTENT(IN) ::  fix(nfix)
   REAL(kind=4), INTENT(IN) ::  delt
   REAL(kind=8), INTENT(IN) ::  rconst(nreact), bgs(nprcf)
   REAL(kind=8), INTENT(IN) ::  AbsTol(N), RelTol(N)
   INTEGER(kind=4), INTENT(OUT) :: IERR
   REAL(kind=8), INTENT(INOUT) :: H, Y(N)
!!if_off
!~~~~ rodas3 parameters
   INTEGER(kind=4), parameter ::  ros_S=4
   REAL(kind=8), parameter :: ros_ELO = 3.0d0
   REAL(kind=8), parameter, dimension(6) :: ros_A = &
            (/ 0.0d0, 2.0d0, 0.0d0, 2.0d0, 0.0d0, 1.0d0/)
   REAL(kind=8), parameter, dimension(6) :: ros_C = &
            (/ 4.0d0, 1.0d0,-1.0d0, 1.0d0,-1.0d0,-(8.0d0/3.0d0)/)
   REAL(kind=8), parameter :: ros_Gamma = 0.5d0
! LU_NONZERO - Number of nonzero entries in LU factoriz. of Jacobian
  INTEGER, PARAMETER :: LU_NONZERO = 432
!~~~~ local variables
   integer(kind=4) :: nstp
   REAL(kind=8) :: T, Tstart,Tend, Hmin, Hmax
   REAL(kind=8) :: Roundoff
   REAL(kind=8) :: Ynew(N), Fcn0(N), Fcn(N)
   REAL(kind=8) :: K(N*ros_S)
   REAL(kind=8) :: Jac0(LU_NONZERO), Ghimj(LU_NONZERO)
   REAL(kind=8) :: Hnew, HC, Fac, Hexit
   REAL(kind=8) :: Err, Yerr(N)
   INTEGER(kind=4) :: ioffset, j, istage
   LOGICAL :: RejectLastH, RejectMoreH, Singular
!~~~>  Local parameters
   INTEGER(kind=4), parameter :: Max_no_steps=200000
   REAL(kind=8), PARAMETER :: ZERO = 0.0d0, ONE  = 1.0d0
   REAL(kind=8), parameter :: FacMin=0.2d0, FacMax=6.0d0, &
                              FacRej=0.1d0, FacSafe=0.9d0
   REAL(kind=8), PARAMETER :: DeltaMin = 1.6666667d-7

   EXTERNAL DSCAL, DAXPY

!   write(0,*) 'in rodas3: rconst=', rconst
!   write(0,*) 'in rodas3: bgs=', bgs
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
!~~~>  Initial preparations
   Roundoff = epsilon(ONE)
   nstp = 0
   Tstart = ZERO
   Tend = Tstart + dble(delt)
   T = Tstart
   Hexit = ZERO
   Hmin = ZERO
   Hmax = dble(delt)
   IF (H <= 10.0d0*Roundoff) H = DeltaMin
   RejectLastH=.FALSE.
   RejectMoreH=.FALSE.
!~~~> Time loop begins below
TimeLoop: DO WHILE ( (T-Tend)+Roundoff <= ZERO )
   IF ( Nstp > Max_no_steps ) THEN
      IERR=-6
      RETURN
   END IF
   IF ( ((T+0.1d0*H) == T).OR.(H <= Roundoff) ) THEN
      IERR=-7
      RETURN
   END IF
!~~~>  Limit H if necessary to avoid going beyond Tend
   Hexit = H
   H = MIN(H,Tend-T)
!~~~>   Compute the function at current time
   CALL Fun_day( Y, FIX, RCONST, BGS, Fcn0, n, nfix, nreact, nprcf )
!~~~>   Compute the Jacobian at current time
   CALL Jac_SP_day( Y, FIX, RCONST, BGS, Jac0, n, nfix, nreact, nprcf, LU_NONZERO )
!~~~>  Repeat step calculation until current step accepted
UntilAccepted: DO
    CALL ros_PrepareMatrix_day(H,ros_Gamma,Jac0,Ghimj,Singular)
   IF (Singular) THEN
       IERR=-8
       RETURN
   END IF
!~~~>   Compute the stages
Stage: DO istage = 1, ros_S

       ioffset = N*(istage-1)

       IF ( istage == 1 ) THEN
         Fcn(1:N) = Fcn0(1:N)

       ELSEIF ( istage == 3 ) THEN
         do j = 1, N
           Ynew(j) = Y(j) + ros_A(2)*K(j)
         enddo
         CALL Fun_day( Ynew, FIX, RCONST, BGS, Fcn, n, nfix, nreact, nprcf )
       ELSEIF ( istage == 4 ) THEN
         do j=1,N
            Ynew(j) = Y(j) + ros_A(4)*K(j) + ros_A(6)*K(2*N+j)
         enddo
         CALL Fun_day( Ynew, FIX, RCONST, BGS, Fcn, n, nfix, nreact, nprcf )
       END IF
       K(ioffset+1:ioffset+N)=Fcn(1:N)
       DO j = 1, istage-1
         HC = ros_C((istage-1)*(istage-2)/2+j)/H
!         do i = 1, N
!            K(ioffset+i) = K(ioffset+i) + HC*K(N*(j-1)+i)
!         end do
         CALL DAXPY(N,HC,K(N*(j-1)+1),1,K(ioffset+1),1)
       END DO
       CALL KppSolve_day( Ghimj, K(ioffset+1),N,LU_NONZERO)
   END DO Stage
!~~~>  Compute the new solution and the error estimation
   DO j = 1, N
      Ynew(j) = Y(j) + 2.0d0*K(j) + K(2*N+j) + K(3*N+j)
      Yerr(j) = K(3*N+j)
   END DO
   Err = ros_ErrorNorm ( Y, Ynew, Yerr, AbsTol, RelTol )
!~~~> New step size is bounded by FacMin <= Hnew/H <= FacMax
   Fac  = MIN(FacMax,MAX(FacMin,FacSafe/Err**(ONE/ros_ELO)))
   Hnew = H*Fac
!~~~>  Check the error magnitude and adjust step size
   Nstp = Nstp + 1
   IF ( (Err <= ONE).OR.(H <= Hmin) ) THEN
      Y(1:N) = Ynew(1:N)
      T = T + H
      Hnew = MAX(Hmin,MIN(Hnew,Hmax))
      IF (RejectLastH) THEN
         Hnew = MIN(Hnew,H)
      END IF
      RejectLastH = .FALSE.
      RejectMoreH = .FALSE.
      H = Hnew
      EXIT UntilAccepted
   ELSE
      IF (RejectMoreH) THEN
         Hnew = H*FacRej
      END IF
      RejectMoreH = RejectLastH
      RejectLastH = .TRUE.
      H = Hnew
   END IF
   END DO UntilAccepted
   END DO TimeLoop
!~~~> Succesful exit
   IERR = 1
   H = Hexit

  RETURN

